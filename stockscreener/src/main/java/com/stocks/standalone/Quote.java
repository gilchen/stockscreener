package com.stocks.standalone;

public class Quote {
	private String symbol;
	private String realTime;
	private String rangeToday;
	private String pcChange;
	private String time;
	private String range52wk;
	private String range52wkPc;
	
	public String getSymbol() {
		return symbol;
	}
	public void setSymbol(String symbol) {
		this.symbol = symbol;
	}
	public String getRealTime() {
		return realTime;
	}
	public void setRealTime(String realTime) {
		this.realTime = realTime;
	}
	public String getRangeToday() {
		return rangeToday;
	}
	public void setRangeToday(String rangeToday) {
		this.rangeToday = rangeToday;
	}
	public String getPcChange() {
		return pcChange;
	}
	public void setPcChange(String pcChange) {
		this.pcChange = pcChange;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getRange52wk() {
		return range52wk;
	}
	public void setRange52wk(String range52wk) {
		this.range52wk = range52wk;
	}
	public String getRange52wkPc() {
		return range52wkPc;
	}
	public void setRange52wkPc(String range52wkPc) {
		this.range52wkPc = range52wkPc;
	}
	
	
}
